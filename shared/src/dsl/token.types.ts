// Типы токенов для лексера DSL интерактивных видео-сценариев.
//
// Ключевые слова определяются лексером после прочтения идентификатора
// (lookup-таблица в lexer.ts). Это стандартный подход: сначала читаем
// IDENTIFIER-последовательность, потом проверяем, не ключевое ли слово.

import type { Span } from './ast.types.js';

// ─────────────────────────────────────────────
// ВИДЫ ТОКЕНОВ
// ─────────────────────────────────────────────

/**
 * Вид токена.
 *
 * const enum вместо обычного enum позволяет TypeScript инлайнить строковые
 * значения при компиляции — нет runtime-объекта перечисления, что важно
 * для shared-пакета с target ESM.
 */
export type TokenKind =
  // ─── Ключевые слова ───────────────────────────────────────────────────────
  | 'SCENE'        // scene
  | 'CHARACTER'    // character
  | 'SET'          // set
  | 'TEXT'         // text
  | 'VIDEO'        // video
  | 'FROM'         // from
  | 'TO'           // to
  | 'CHOICE'       // choice
  | 'WHEN'         // when
  | 'GOTO'         // goto
  | 'IF'           // if
  | 'ELSE'         // else
  | 'DESCRIPTION'  // description (внутри character { })
  // ─── Ключевые слова-операторы ─────────────────────────────────────────────
  | 'AND'          // and
  | 'OR'           // or
  | 'NOT'          // not
  // ─── Булевы литералы ──────────────────────────────────────────────────────
  | 'TRUE'         // true
  | 'FALSE'        // false
  // ─── Литералы ─────────────────────────────────────────────────────────────
  | 'STRING'       // "..." (value = содержимое без кавычек, escape развёрнут)
  | 'NUMBER'       // 42, 3.14 (value = строка; число парсится в parser.ts)
  | 'IDENTIFIER'   // имена сцен, переменных, персонажей
  // ─── Двухсимвольные операторы ─────────────────────────────────────────────
  | 'ARROW'        // ->
  | 'EQ'           // ==
  | 'NEQ'          // !=
  | 'LTE'          // <=
  | 'GTE'          // >=
  // ─── Односимвольные операторы ─────────────────────────────────────────────
  | 'ASSIGN'       // =  (одиночный, не ==)
  | 'LT'           // <
  | 'GT'           // >
  | 'PLUS'         // +
  | 'MINUS'        // -
  | 'STAR'         // *
  | 'SLASH'        // /
  // ─── Пунктуация ───────────────────────────────────────────────────────────
  | 'LBRACE'       // {
  | 'RBRACE'       // }
  | 'LPAREN'       // (
  | 'RPAREN'       // )
  | 'COMMA'        // ,
  | 'COLON'        // :
  // ─── Специальные ──────────────────────────────────────────────────────────
  | 'EOF'          // конец входного потока
  | 'UNKNOWN';     // неизвестный символ (лексер не бросает исключение, продолжает)

// ─────────────────────────────────────────────
// ТОКЕН
// ─────────────────────────────────────────────

/**
 * Токен — минимальная смысловая единица исходного текста.
 *
 * `span` хранит позицию начала и конца токена; используется в двух местах:
 *   1. Парсер передаёт его в поле `span` узла AST.
 *   2. Анализатор формирует Diagnostic с позицией, которую Monaco
 *      отображает как marker (подчёркивание в редакторе).
 */
export interface Token {
  kind: TokenKind;
  /**
   * Исходный текст токена.
   * Для STRING — содержимое без обрамляющих кавычек, escape-последовательности
   * уже развёрнуты (\" → ", \n → перенос строки).
   * Для UNKNOWN — неизвестный символ как есть.
   * Для EOF — пустая строка.
   */
  value: string;
  /** Диапазон токена в исходном тексте. */
  span: Span;
}
