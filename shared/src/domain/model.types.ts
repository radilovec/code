// Типы доменной модели — T2.2.
// Доменная модель — это «бизнес-объекты», с которыми работает UI
// (редактор, граф, плеер). Строится из AST через builder (T2.6).
//
// В отличие от AST, здесь нет позиций и Span — они нужны только
// для диагностики в редакторе Monaco. Исключение: Diagnostic
// (нижняя секция) — он содержит позицию для Monaco markers.

import type { Expression } from '../dsl/ast.types.js';

// ─────────────────────────────────────────────
// CONDITION — условие выбора (compile-time из AST Expression)
// ─────────────────────────────────────────────

/**
 * Условие отображения выбора.
 * Хранит оригинальный AST Expression (для интерпретатора) и
 * строковое представление (для UI, чтобы не пересчитывать каждый рендер).
 *
 * Принцип: условие хранится как безопасный AST, а не как JavaScript-код.
 * Это закрывает атаку на исполнение пользовательского кода (source.md, раздел "Вывод").
 */
export interface Condition {
  /** Оригинальное AST-выражение — используется интерпретатором при прохождении. */
  expression: Expression;
  /** Человекочитаемое представление для UI (напр. "hp >= 50"). */
  displayText: string;
}

// ─────────────────────────────────────────────
// VARIABLE — глобальная переменная сценария
// ─────────────────────────────────────────────

/** Поддерживаемые типы значений переменных. */
export type VariableType = 'string' | 'number' | 'boolean';

/**
 * Глобальная переменная сценария.
 * Объявляется через `set varName = initialValue` в начале сценария.
 * Тип выводится из начального значения семантическим анализатором (T2.5).
 */
export interface Variable {
  name: string;
  type: VariableType;
  /** Начальное значение (из VarDecl). */
  initialValue: string | number | boolean;
}

// ─────────────────────────────────────────────
// CHARACTER — персонаж сценария
// ─────────────────────────────────────────────

/**
 * Персонаж сценария.
 * Объявляется через `character name { description "..." }`.
 * `mentionedInScenes` — производное поле: заполняется builder'ом T2.6
 * сканированием TextStmt с `characterName` по всем сценам.
 * Используется в P1-фиче справочника персонажей (T9.1).
 */
export interface Character {
  name: string;
  description: string;
  /** ID сцен, где персонаж упомянут (TextStmt с characterName). Производный. */
  mentionedInScenes: string[];
}

// ─────────────────────────────────────────────
// VIDEO SEGMENT — видео-сегмент сцены
// ─────────────────────────────────────────────

/**
 * Привязка видео-сегмента к сцене.
 * URL — внешний (YouTube, Vimeo, прямая ссылка на mp4).
 * Загрузка видео на сервер не предусмотрена (source.md, ivid/ivid).
 * `from` и `to` — таймкоды в секундах; тип `number` для прямого
 * использования в `video.currentTime` HTML5 Video API.
 */
export interface VideoSegment {
  url: string;
  /** Таймкод начала сегмента, секунды. */
  from: number;
  /** Таймкод конца сегмента, секунды. */
  to: number;
}

// ─────────────────────────────────────────────
// CHOICE — вариант выбора из сцены
// ─────────────────────────────────────────────

/**
 * Вариант выбора, отображаемый пользователю.
 * Без `condition` — всегда виден.
 * С `condition` — плеер вычисляет условие через интерпретатор (T2.7)
 * и скрывает выбор, если результат `false`.
 */
export interface Choice {
  /** Текст кнопки выбора (отображается пользователю). */
  label: string;
  /** ID целевой сцены при выборе этого варианта. */
  targetSceneId: string;
  /** Опциональное условие отображения (`when <condition>` в DSL). */
  condition?: Condition;
}

// ─────────────────────────────────────────────
// SCENE — сцена сценария
// ─────────────────────────────────────────────

/**
 * Тип сцены — определяется семантическим анализатором при построении модели.
 * - `normal`  — обычная сцена с видео и выборами.
 * - `choice`  — сцена, где выборы определены явно (choice-инструкции).
 * - `ending`  — концовка: нет исходящих переходов (choices пусты, нет autoTransitionTo).
 */
export type SceneType = 'normal' | 'choice' | 'ending';

/**
 * Сцена сценария — основная единица нарратива.
 *
 * Поля `id` и `name` совпадают в MVP (имя из DSL = ID в системе).
 * `unreachable` заполняется семантическим анализатором (T2.5), а не builder'ом —
 * разделение ответственности: builder строит структуру, анализатор проверяет
 * семантику (достижимость, корректность переходов).
 */
export interface Scene {
  /** Уникальный ID сцены (= имя из DSL `scene <name> { ... }`). */
  id: string;
  /** Отображаемое имя (совпадает с id в MVP). */
  name: string;
  /** Тип сцены (заполняется анализатором T2.5). */
  type: SceneType;
  /** Видео-сегмент (опционально — сцена может быть без видео). */
  video?: VideoSegment;
  /** Нарративные тексты в порядке объявления в DSL. */
  texts: string[];
  /** Варианты выбора (пустой список = концовка или автопереход). */
  choices: Choice[];
  /**
   * Безусловный автопереход (из GoTo-инструкции в DSL).
   * Если задан — choices должны быть пустыми.
   */
  autoTransitionTo?: string;
  /**
   * True, если сцена недостижима из стартовой.
   * Заполняется семантическим анализатором (T2.5), не builder'ом.
   * Используется для отображения предупреждений в редакторе и графе (T5.5).
   */
  unreachable: boolean;
}

// ─────────────────────────────────────────────
// SCENARIO — корневая доменная модель сценария
// ─────────────────────────────────────────────

/**
 * Корневая модель сценария — то, с чем работает весь UI редактора.
 *
 * Использует `Map<string, Scene>` вместо массива — O(1) поиск по ID
 * при интерпретации (`currentScene = scenario.scenes.get(targetId)`).
 * Плеер выполняет такой поиск на каждом переходе — производительность важна.
 *
 * `startSceneId` — ID первой сцены. Определяется как первая объявленная
 * сцена в DSL (порядок объявлений сохраняется парсером).
 */
export interface Scenario {
  /** ID стартовой сцены (первая объявленная в DSL). */
  startSceneId: string;
  /** Все сцены сценария, ключ — id сцены. */
  scenes: Map<string, Scene>;
  /** Все переменные сценария, ключ — имя переменной. */
  variables: Map<string, Variable>;
  /** Все персонажи сценария, ключ — имя персонажа. */
  characters: Map<string, Character>;
}

// ─────────────────────────────────────────────
// GRAPH NODE / EDGE — для Foblex Flow (T5.x)
// ─────────────────────────────────────────────
// Производные типы для графа. Здесь только типы данных;
// компонент графа сам преобразует Scenario → GraphData (T5.2).
// Координаты заполняет dagre auto-layout (T5.3) или сохранённая
// раскладка пользователя из Layout-сущности в БД.

/** Визуальный тип узла графа — определяет форму и цвет в Foblex Flow (T5.4). */
export type GraphNodeType = 'scene' | 'choice' | 'ending';

/** Узел графа — соответствует одной сцене в Scenario. */
export interface GraphNode {
  /** ID совпадает с Scene.id. */
  id: string;
  type: GraphNodeType;
  /** Отображаемое имя на узле. */
  label: string;
  /** X-координата центра узла (после dagre layout или из сохранённой раскладки). */
  x: number;
  /** Y-координата центра узла. */
  y: number;
  width: number;
  height: number;
  /** Если true — узел подсвечивается как недостижимый (T5.5). */
  unreachable: boolean;
}

/** Ребро графа — соответствует одному Choice. */
export interface GraphEdge {
  /** Уникальный ID ребра (напр. `${sourceId}__${targetId}__${label}`). */
  id: string;
  sourceId: string;
  targetId: string;
  /** Текст выбора (label из Choice). Опционально — автопереход GoTo не имеет label. */
  label?: string;
  /** True, если Choice имеет условие — ребро отображается иначе в графе (T5.4). */
  hasCondition: boolean;
}

/** Полные данные для рендера графа в Foblex Flow. */
export interface GraphData {
  nodes: GraphNode[];
  edges: GraphEdge[];
}

// ─────────────────────────────────────────────
// DIAGNOSTICS — результат семантического анализа
// ─────────────────────────────────────────────

/** Уровень критичности диагностического сообщения. */
export type DiagnosticSeverity = 'error' | 'warning' | 'info';

/**
 * Диагностическое сообщение от семантического анализатора (T2.5).
 * Структура намеренно дублирует `monaco.editor.IMarkerData` —
 * для прямого маппинга без трансформаций на фронтенде (T4.6).
 *
 * Поля `line`/`column`/`endLine`/`endColumn` — 1-based (Monaco API).
 */
export interface Diagnostic {
  severity: DiagnosticSeverity;
  message: string;
  /** Начальная строка диапазона ошибки, 1-based. */
  line: number;
  /** Начальный столбец, 1-based. */
  column: number;
  /** Конечная строка диапазона ошибки, 1-based. */
  endLine: number;
  /** Конечный столбец, 1-based. */
  endColumn: number;
}
