/**
 * Состояние runtime-интерпретатора.
 *
 * Словарь «имя переменной → текущее значение». Используется и в авторинге
 * (для предварительной проверки), и в плеере (для реального исполнения).
 *
 * Специальный ключ `__goto` может быть установлен инструкцией GoTo —
 * плеер проверяет его после execute() для перехода к целевой сцене.
 */
export type RuntimeState = Record<string, number | string | boolean>;
